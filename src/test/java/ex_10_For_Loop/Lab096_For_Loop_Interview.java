package ex_10_For_Loop;

public class Lab096_For_Loop_Interview {
    public static void main(String[] args) {
        for(int i=0;i>10;){
            System.out.println("Hello");
        }
    }
}

//No increment statement is present (though it's optional).
//Initially, i = 0, and the condition is i > 10, which is false.
//Since the condition fails immediately, the loop never executes.