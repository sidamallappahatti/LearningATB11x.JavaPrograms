package ex_11_While;

public class Lab108_While {
    public static void main(String[] args) {
        int i=0;
        while(i>0){
            System.out.println("Enter");
            System.out.println(i);
            i++;
        }
    }
}

//Since the condition is false from the beginning, the loop never executes.
//As a result, nothing is printed on the console.
