package ex_01_java_basics_02;

public class Lab011_Variables {
    public static void main(String[] args) {

        // datatype variable_name=variable_value

        byte b=20;
        b=15;

        System.out.println(b);
        // byte is data type it's a type of container(Variable)
        // stores only integer value but limited to max and min
        // -128 to 127

        // b ->  variable name -> identifier

        // = -> assignment operator - assigns the value to the b

        // 20 -> literal -> integer value
    }
}
